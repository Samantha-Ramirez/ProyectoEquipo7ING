package main.Model.gestionCartas;

public class CartaCompromiso {
    private String acuerdoRetribucion;
    private String formularioGeneracion;

    // Getters y Setters
    public String getAcuerdoRetribucion() {
        return acuerdoRetribucion;
    }

    public void setAcuerdoRetribucion(String acuerdoRetribucion) {
        this.acuerdoRetribucion = acuerdoRetribucion;
    }

    public String getFormularioGeneracion() {
        return formularioGeneracion;
    }

    public void setFormularioGeneracion(String formularioGeneracion) {
        this.formularioGeneracion = formularioGeneracion;
    }
}